const delay = (ms: number) => new Promise(resolve => setTimeout(resolve, ms));

// Mock API function to fetch companies with filters and pagination
export async function fetchCompanies(
  filters: FilterOptions,
  page: number = 1,
  itemsPerPage: number = 10
): Promise<{ companies: Company[]; pagination: PaginationInfo }> {
  // Simulate API call delay
  await delay(500);

  // Simulate random errors (3% chance)
  if (Math.random() < 0.03) {
    throw new Error('Failed to fetch companies. Please try again.');
  }

  let filteredCompanies = [...mockCompanies];

  // Apply search filter
  if (filters.search) {
    const searchLower = filters.search.toLowerCase();
    filteredCompanies = filteredCompanies.filter(company =>
      company.name.toLowerCase().includes(searchLower) ||
      company.description.toLowerCase().includes(searchLower) ||
      company.country.toLowerCase().includes(searchLower)
    );
  }

  // Apply industry filter
  if (filters.industry && filters.industry !== 'all') {
    filteredCompanies = filteredCompanies.filter(
      company => company.industry === filters.industry
    );
  }

  // Apply location filter
  if (filters.location && filters.location !== 'all') {
    filteredCompanies = filteredCompanies.filter(
      company => company.location === filters.location
    );
  }

  // Apply sorting
  filteredCompanies.sort((a, b) => {
    let comparison = 0;
    
    switch (filters.sortBy) {
      case 'name':
        comparison = a.name.localeCompare(b.name);
        break;
      case 'location':
        comparison = a.location.localeCompare(b.location);
        break;
      case 'industry':
        comparison = a.industry.localeCompare(b.industry);
        break;
      case 'employees':
        comparison = a.employees - b.employees;
        break;
      case 'founded':
        comparison = a.founded - b.founded;
        break;
    }

    return filters.sortOrder === 'asc' ? comparison : -comparison;
  });

  // Calculate pagination
  const totalItems = filteredCompanies.length;
  const totalPages = Math.ceil(totalItems / itemsPerPage);
  const startIndex = (page - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  const paginatedCompanies = filteredCompanies.slice(startIndex, endIndex);

  return {
    companies: paginatedCompanies,
    pagination: {
      currentPage: page,
      totalPages,
      totalItems,
      itemsPerPage,
    },
  };
}

